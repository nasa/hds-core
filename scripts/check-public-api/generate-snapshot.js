import { readFileSync } from 'node:fs';
import { resolve } from 'node:path';
import { extractCustomProperties } from './lib/extract-custom-properties.js';
import { extractSelectors } from './lib/extract-selectors.js';
import { extractSassVariables, extractSassMixins } from './lib/extract-sass.js';
import { validateAdopterLayer } from './lib/validate-adopter-layer.js';

// Paths relative to repo root
const ROOT = resolve(import.meta.dirname, '..', '..');

const ENTRY_POINTS = ['src/scss/hds.scss', 'src/scss/hds-dataviz.scss', 'src/scss/hds-uswds.scss'];

const CSS_BUNDLES = {
  hds: resolve(ROOT, 'dist/css/hds.min.css'),
  dataviz: resolve(ROOT, 'dist/css/hds-dataviz.min.css'),
};

const SASS_FILES = {
  config: resolve(ROOT, 'src/scss/_hds-config.scss'),
  tokens: resolve(ROOT, 'src/scss/_hds-tokens.scss'),
  datavizPalettes: resolve(ROOT, 'src/scss/_hds-dataviz-palettes.scss'),
  mixins: resolve(ROOT, 'src/scss/_hds-mixins.scss'),
  typography: resolve(ROOT, 'src/scss/_hds-typography.scss'),
};

const CSS_COMPONENT_FILES = {
  componentProperties: resolve(ROOT, 'src/scss/base/_component-properties.scss'),
};

/**
 * Generate the full snapshot text from current build output and source files.
 * Emits warnings to stderr for rules outside @layer.
 * Returns the snapshot string.
 */
export function generateSnapshot() {
  const hdsCss = readFileSync(CSS_BUNDLES.hds, 'utf-8');
  const datavizCss = readFileSync(CSS_BUNDLES.dataviz, 'utf-8');

  // Adopter layer check
  const adopterLayer = validateAdopterLayer(hdsCss);

  // Custom properties
  const hdsProps = extractCustomProperties(hdsCss);
  const datavizProps = extractCustomProperties(datavizCss);

  // Selectors (combined from both bundles)
  const hdsSelectors = extractSelectors(hdsCss);
  const datavizSelectors = extractSelectors(datavizCss);

  // Merge selectors and deduplicate
  const allSelectors = [...new Set([...hdsSelectors.selectors, ...datavizSelectors.selectors])].sort();

  // Emit warnings to stderr
  const allWarnings = [...hdsSelectors.warnings, ...datavizSelectors.warnings];
  for (const warning of allWarnings) {
    process.stderr.write(`⚠️  ${warning}\n`);
  }

  // Sass extraction
  const configVars = extractSassVariables(SASS_FILES.config);
  const tokenVars = extractSassVariables(SASS_FILES.tokens);
  const datavizVars = extractSassVariables(SASS_FILES.datavizPalettes);
  const mixins = extractSassMixins(SASS_FILES.mixins);
  const typographyVars = extractSassVariables(SASS_FILES.typography);
  const typographyMixins = extractSassMixins(SASS_FILES.typography);

  // Build snapshot
  const lines = [
    '# HDS Core Public API Surface',
    '# Auto-generated by scripts/check-public-api/generate-snapshot.js',
    '# Do not hand-edit. Run `npm run update:api-snapshot` to regenerate.',
    '',
    '## Entry Points',
    ...ENTRY_POINTS,
    '',
    '## Adopter Layer',
    adopterLayer,
    '',
    '## Custom Properties (hds.min.css)',
    ...hdsProps,
    '',
    '## Custom Properties (hds-dataviz.min.css)',
    ...datavizProps,
    '',
    '## Sass Variables (_hds-config.scss)',
    ...configVars,
    '',
    '## Sass Variables (_hds-tokens.scss)',
    ...tokenVars,
    '',
    '## Sass Variables (_hds-typography.scss)',
    ...typographyVars,
    '',
    '## Sass Variables (_hds-dataviz-palettes.scss)',
    ...datavizVars,
    '',
    '## Sass Mixins (_hds-mixins.scss)',
    ...mixins,
    '',
    '## Sass Mixins (_hds-typography.scss)',
    ...typographyMixins,
    '',
    '## Selectors — HDS Authored',
    ...allSelectors,
    '',
  ];

  return lines.join('\n');
}
